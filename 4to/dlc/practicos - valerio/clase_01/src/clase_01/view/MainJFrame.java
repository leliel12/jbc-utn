    /**
     * muestra un navegador de archivos que solo muestra dicrectorios
     * y reotrna el directorio seleccionado
     * @return directory
     */

    private File fileChooser() {
        JFileChooser jfd = new JFileChooser();
        jfd.setFileSelectionMode(JFileChooser.DIRECTORIES_ONLY);
        if (jfd.showOpenDialog(null) == JFileChooser.APPROVE_OPTION) {
            return jfd.getSelectedFile();
        }
        return null;
    }

